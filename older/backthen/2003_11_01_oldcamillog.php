<h5>
  Tuesday, November 18, 2003
</h5>

<p class="useit">
  <b>Do I have to have a green car to live in the US?</b><br />
I had no idea that a green car was required to live and work in the US. This add below was one I found on a pretty serious site like Yahoo, built into my webmail. 
<img class="bot" src="http://xoc.dk/older/images/things/GreenCar.gif"  alt="Green Car" />
</p>
<p>
It's in Danish, so I'll translate. It says: <i>"Live and work in the United States of America" - "You can win a Green Car to Live and Work in the US". Click here to be registered online"</i></p>
<p>
First of all, I'm appalled by the fact that the US has a lottery where you can win the right to stay in God's own country. <br />
Second, I am a little confused by the translation into Danish. Did they really mean "Green Card"? Or is that just a movie with a French guy with a big nose? Or does your choice of vehicle really matter? In my case I would have a hard time being allowed to Live and Work in the US: I only have a bike, and it's not even green - i'ts black.<br />
Third, why is "Live and Work" capitalized? As far as I know, verbs are never capitalized in Danish unless they start a sentence. I'm pretty sure i'ts the same in English. 
</p>
<p>
If I see an add with errors I never buy the product, but I don't really know what to do with this one? Not buy a lottery ticket (they are expensive too) I guess.
</p>

