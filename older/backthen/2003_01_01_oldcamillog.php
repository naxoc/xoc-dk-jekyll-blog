<b>I can't believe my luck</b><br />
When I go running I always try to run as much as possible on
the grass in the park instead of on the paths. I do that
because I have an old injury from when I played handball
that makes my shins sore as hell if I run on a hard surface.
People look a little puzzled when I make my way through the
mud, but hey! I don't have to worry about crashing into
other runners when I avoid the paths. Anyway, today as I a
fought my way over the lawn in the park I found a really
cool and expensive cell phone just lying in the grass and
not a soul in sight. I picked it up and brought it home. I
had to pour the water out of it, but after doing that I
could put my SIM-card in it and turn it on. Yay! </p>
<p>
This could not have happened at a better time because my old
phone is quickly dying (phones today must be designed to
last 1 year max.). I am so lucky! Morale: travel by the path
not often taken:-)</p>
<p>
Oh, and if you lost your phone in the park today, please
e-mail me at <a href="mailto:luckybastard@yourenotgonnagetit.com">
luckybastard@yourenotgonnagetit.com</a>.
</p>

