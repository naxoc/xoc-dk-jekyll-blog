<b>Pretzel flipz? - Yuck!</b><br />
January is the prime time of the year for diets. All
magazines here in Denmark have some sort small booklet
attached to them full of healthy recipes. I think it's a
craze but on the other hand I demand my right to moan about
my own figure at least 6 times daily so I guess I'm part of
it how ever much I hate it.</p>
<p>
Today I started out neatly with a
strawberry-grapefruit-banana smoothie for breakfast, went on
to a salmon sandwich for lunch and for supper I had steamed
cod with lots of vegetables. That is good food for a diet
right? The thing is I just quit smoking so I get these weird
cravings (I once heard someone describe smoking as an oral
fixation). So besides for my 3 meals today I had: 50g of raw marzipan, a bag of popcorn, some foul chocolate
thingies, a bagel-like bread with sesame seeds, loads of
sugary lemonade, and of course a lot of café latte with
sugar and milk.</p>
<p>
My point in sharing my food intake today with you is that
the diets in the magazines don't work! I ate some really
healthy food for all my meals today but I have a sneaking
suspicion that I will not have lost weight today. Just
because you "fry" your chicken without fat it probably won't
make your cravings for odd snacks like <a href="http://www.somethingawful.com/#EpukZApEAkbwWmtnio">
pretzel flipz</a> go away. The magazines will have to do better than that.
-They could start out by choosing some models that didn't
have anorexia. If they did that maybe the designer would
start to make clothes in sizes that fit ordinary people. And
if.. OK, now I'm rambling. I just get so tired of this whole
diet show every year. And I get even more annoyed that I let
myself be part of it.
</p>

