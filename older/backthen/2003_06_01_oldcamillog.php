<b>An interview with a weblogger</b><br />
I promised that I would get back to what the people from the sociology 
department at the university wanted to ask me about weblogging. They asked about all sorts of things, 
but the questions below are those that got me thinking:
</p>
<ul>
  <li>
    <b>Why do you have a weblog?</b><br />
    Good one... Hmm. It must be the need to express myself on the web in an informal way. Other people make a site 
    about their hobbies or whatever, but since I don't really feel that what I do every day can be categorized, I have 
    a weblog. A great place to put silly links, musings, rants, and just thoughts.
  </li>
  <li>
    <b>Would you ever post something very personal?</b><br />
    No! .. err.. and yes.. I don't know. People that know me know that very personal things I only discuss with close friends. 
    I am not the person to talk about a fungal infection (bad example) at a party. People that know my weblog also know that I don't 
    post personal stuff often. It's not that I have a fear of the putting stuff like that on the web, but it's just no different from 
    how I would go about something personal in real life. <br />
    The one thing that could worry be a little about having stuff like I'm gay on the web is when I apply for jobs. I would definitely 
    tell people at work that I'm gay, but maybe not the first day. I wouldn't like for them to read it on the internet first, I would 
    like to tell it to them in person.
  </li>
  <li>
    <b>Do you know any of the people you link to personally?</b><br />
    Only <a href="http://verture.net">Jonas</a>, I go to school with him. All the others live far away. 
    It would be funny to meet them though. I feel like I know them because reading people's weblogs for some time 
    gives a little insight in their personality. One of the things that the web-media lacks, is seeing peoples faces 
    when they talk though, that gives a much more nuanced impression of irony and such.
    I dont have many links to other weblogs on my page, but the ones I have I read on a daily basis. I 
    simply don't have time to read many more weblogs and therefore I don't link to others.
  </li>  
  <li>
    <b>How much time do you spend on your weblog every day?</b><br />
    My own? Not much.. It's like every time I have an idea for a post I either forget it or I don't have time to write.
    I spend more time reading other peoples weblogs. Only when I get 
    a redecorating fit (that for instance turns my page very orange) do I spend more 
    than 10 minutes a day. 
  </li>  
</ul>
<p>
It was great fun to be asked a lot of questions about weblogging, it had me thinking about a lot of things. Especially the "why do 
you have a weblog" was funny. I never thought about that - I just do.
</p>

