<p class="useit">
  <b>Ouch! My arm!</b><br />
<img class="bot"src="https://xoc.dk/older/images/things/CAndersen.jpg"  alt="camilla" />
I used to get a lot more exercise than I do now. I played
volleyball and I worked as a bike messenger until about two
years ago. Since then I guess that the wildest exercise I
have been getting is when I lift a chair from one room into
the other room where the computer is. I have been dancing
though, and that is real good fun, but you don't really work
up a sweat doing that. </p>
<p>
Now I have decided to play handball again in the Copenhagen
gay sports club PAN-idr&aelig;t. For those of you (that would be
the Americans) that don't know this fine sport, I can
explain that it is a lot like soccer, except it's indoors,
the field is smaller and you can take the ball up in your
hand - OK. It's not at all like soccer. Maybe the picture of
the game explains it better. 
I should probably also say that the game does not give extra
points for taking the opponents shirts of.  </p>
<p>
Anyway, I played this Tuesday and my arm has felt like it's
about to fall of ever since. I hereby promise myself that I
will never go without exercise for that long again.

</p>

