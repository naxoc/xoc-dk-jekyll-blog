<b>The ethics of Java programming</b><br />
I must warn you that this post is very geeky.. I was at a lecture today on 
Java and we were talking about a method called clone() - a method that makes 
an exact copy of any object. As my thoughts wandered I realized that no one 
had really considered what happens when you clone human objects for instance. 
What if I had a George Bush object from my dictator class? Would I really want 
to clone that? <br />
I suddenly have my doubts about object-oriented programming. Has any one really 
discussed the ethics of using clone()?
