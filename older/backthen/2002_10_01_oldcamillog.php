<p class="useit">
  <b>Your sweetest one?</b><br />
In Danish the word for girlfriend/boyfriend is "k&aelig;reste". Its kind of a sweet word I think, meaning
something like "the sweetest one" (poor translation, but it comes close). The confusing thing about this
is that, unlike in English where it would be girl- or boyfriend, it does not reveal the sex of the
sweetest one. I'm not in the closet but I don't have a need to tell everybody about my sexuality the
first day I meet them either. When I talk to new people I use the word "k&aelig;reste" about my
girlfriend as does everybody else. That way months can pass without my new friends knowing I'm gay, if
they don't ask my "k&aelig;reste's" name. Then it becomes really awkward when they come to your house and
read the names on the door, or when they ask what "he" does for a living. <br />
I'm still looking for a casual way to come out to people, but I guess the Danish language is not going to
help me out on this one.
</p>

