relationship with real money (i.e. cash) is completely screwed up. I
haven't had more than 200 Danish kroner (25 US$) on me at any time for
the last 10 years. Now I have to learn to go to the bank, take out the
money I need for say a week, and the walk around with all that cash
without spending it all at once. <br />
I really would like to be a little better at finances. I will try my
very best to be a good girl and spend my money in a sensible way and
try not to think about magic when I see an ATM... Just after I buy that
skirt I saw the other day.. Sigh!
