<h5>
  Friday, February 28, 2003
</h5>

<p class="useit">
    <img class="bot" src="http://xoc.dk/older/images/bots/devilguy.gif" alt="devilguy" />
      
<b>Finally! An update!</b><br />
I have been really busy these last two weeks. I must admit that it
might not be the best timing in the world to go on a (very) long
weekend just when the semester starts. By the time I got back from
Norway I hadn't read the 300 pages I brought with me of super-sexy
stuff about Java, databases, and networking protocols. That kind of
left me with a lot of reading when I got home, but who cares? I had an
excellent time up North. </p>
<p>
We drove to where the cabin is, near Lillehammer, in my parents car.
They had generously agreed to switch cars with us for the occasion and
we appreciated that very, very much because five people, a lot of
food, and even more beer and liquor in a Citro&euml;n Saxo for 1100 km's.
would probably have been a little too cosy. The road was a little icy
and mountainous the last 40 km's, but luckily we had Hege, who is a
Norwegian, with us and she certainly knew some tricks as to how to
drive in the mountains in winter -not something they teach Danes when
we take our drivers license (maybe because the highest "mountain" here
is less than 150 meters high). Anyway, we got there and the cabin was
really nice and cosy.
</p>
<p>
Day one on the slopes went really well until late afternoon, the snow
was just perfect. Mette, who was a first time skier, crashed and hurt
her knee. Not good, but she was in good hands because of the five of
us there was a physiotherapist, a nurse (that would be Mette), a
psychologist, and then Birgitte and I, who would certainly have helped
if her computer needed reinstalling (that was not the case though). To
console ourselves we took to the other designated goal of our tour: to
get drunk. That worked fine, I can really recommend gin and tonic with
fresh snow. Under the influence of this fine drink we had great fun
running around in small and at times very transparent t-shirts and
jumping in the snow. We took Mette to the emergency room the next day,
where they told her that her knee was too swollen to diagnose. It
turned out, when we got home, that it was OK.
</p>
<p>
Back home I started my new job as an instructor at my school in
"web-publishing with databases". It's OK fun to have a
studies-relevant job again. Apart from working and studying we work
really hard on painting and shining up our hall (or, entrance room.. I
can't seem to find a less pompous word for that room).
</p>
<p>
There, now I guess I'm all up to date again. I'm sorry that I haven't
been reading much of anyone's weblogs lately, and that I haven't been
commenting as much as I usually do. That won't happen again (until it
happens again).
</p>
<p>
Oh, I'm sorry that the comments here on my site seems to come and go.
Apparently they are doing some updates on Haloscan, so I guess I just
have to be patient. Or write a comments system myself...


</p>

