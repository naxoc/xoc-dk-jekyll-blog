<h5>
  Monday, April 28, 2003
</h5>

<p class="useit">
    <img class="bot" src="http://xoc.dk/older/images/bots/arm.gif" alt="arm" />
      
<b>Empty minded?</b><br />
Today I realized that it is almost a month ago since I last updated my weblog. It's so strange how I sometimes have a lot on my mind and
other times I just exist, with nothing in particular on my mind. Most of my latest posts have been about how I think that Bush is even more
empty minded than me. Writing in your weblog that Bush is an asshole is too easy, it shouldn't really count as a post.
</p>
<p>
Anyway, my site is nearing the 10000 visitor mark. I'm pretty happy with that, and because I wrote a lo-tech script that tracks my visitor's
referrers I fairly much know where people come from when they click on my site. Not everybody visit my site because of my action packed and
funny weblog - most come to see if they can find out if Daniel Radcliffe (Harry Potter) has a girlfriend. I will try to upgrade my referrers
script one of these days so that you can laugh at what people search for. If any of you need a low-tech counter and referrers script (PHP
and mySQL), just <a href="mailto:camilla(a)xoc.dk">e-mail me</a>.
</p>
<p>
Next week the project-writing period starts at my school. That means that I will be twice as busy as I usually am and that I will have even
less time to update my weblog. Funny how every time I am really busy, I start spending a lot of time on stuff not related to what I'm busy
doing. So maybe I should spend some time on the "winterish" style sheet I use now and change it to a "springish" one before the summer
break.<br />
I also want to seriously do something about my books section. People must think that I'm dyslectic as I have had a 100 page book on the
"right now I am reading" for about two months. I actually keep that page for very egoistic reasons, I think that in 20 years it would be
cool to have all the books I read since 2002 in a database.
</p>
<p>
The last goal I have for my weblog is to post something funny now and then. Here is a classic <a href="http://yoga.tripod.co.jp/flash/kikkomaso.swf">superhero film starring a bottle of Kikkoman Soy Sauce</a>. I know it's kind of old, but
it is so funny.<br />
Another site that cracks me up is <a href="http://www.wulffmorgenthaler.com/">Wulffmorgenthaler</a>. It has a great daily comic strip and
the weblog of the Toucan Kid.
</p>

