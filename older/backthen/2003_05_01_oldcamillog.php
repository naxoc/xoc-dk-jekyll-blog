<b>What an inactive weblog</b><br />
My weblog is not my #1 priority now that I am way too busy
to think about anything else than the MSN-messenger clone my
group and I are developing for our project-paper this month.
To quickly fill you in, I have been coding Java so my
fingers fume since May 1. And that's about it. When we
finish the project I a little more than a week, I will of
course make the GoT-messenger (as the baby is called)
available to all of you that might be interested. In the
meantime I think in Java and XML only (which makes
communicating with my girlfriend a little difficult).</p>
