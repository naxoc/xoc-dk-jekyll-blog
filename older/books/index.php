<h4><a href="http://xoc.dk/older/books/default.php?id=20">Tipping the velvet</a></h4>
<p>by <i>Sarah Waters</i></p>
<p class="ten">Genre: Lesbian fiction<br />
Reviewed: September 11, 2003</p>
<p>I had this book recommended over and over from a friend. I
thought "take it easy!" it cannot possibly be that good. But
it is! The story is really a rather simple love story about
a girl that leaves home to go to London to be with her cross
dressing beloved. It is set in late 19th century England and
the author really has that period well described - it's
almost as if one can feel the streets of London. <br />
The main character performs as a singer dressed as a man and
the whole cross dressing thing is very present throughout
the book.</p>
<p>
Anyway. The fantastic thing about this book is the sexual
tension! It is so laden with what one might even call porn,
but in a very stylish, intelligent and intriguing way. Apart
from the fact that story is good, it is very hard to let go
of the book because of the nice <del>sex</del> love scenes, Hehe.  </p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/1860498833/camillog-21"><img class="bot" src="../images/covers/Fingersmith.jpeg" alt="Fingersmith" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=19">Fingersmith</a></h4>
<p>by <i>Sarah Waters</i></p>
<p class="ten">Genre: Lesbian fiction<br />
Reviewed: September 11, 2003</p>
<p>When I had read Tipping the velvet I was hungering for more
of that rush I got from reading it. I read this book that
does not have half the captivating sexual tension, but I
think I like the story in this book better. Also set in late
19th century London this book is about thieves and
scoundrels tricking each other to a degree where they cannot
even figure out who tricked who. I really enjoyed trying to
keep up with the pace the set for tricking each other.</p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/1841190896/camillog-21/202-1841257-2725421"><img class="bot" src="../images/covers/ComicFantasy.jpeg" alt="The mammoth book of (seriously) comic fantasy" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=18">The mammoth book of (seriously) comic fantasy</a></h4>
<p>by <i>Mike Ashley (editor)</i></p>
<p class="ten">Genre: Comic fantasy<br />
Reviewed: September 11, 2003</p>
<p>A collection of short stories some funnier than others, but
almost all had what I like best about fantasy: A hint to
something rotten in our own world. The genre is perfect for
wondering about how man tamed fire or what would happen if
someone hated Cadillacs so much he could make them
disappear.
One story, by Peter Anspach, is absolutely brilliant. It's
the "Top 50 of things I'd do if I ever became an evil
overlord". It is just hilarious. For example:
</p>
<p>
No. 19 My pet monster will be kept in a secure cage from
which it cannot escape and into which I could not
accidentally stumble.<br />
No. 28: If the beautiful princess that I capture says, "I'll
never marry you! Never, do you hear me, NEVER!" I will say,
"Oh, well" and kill her.
</p>
<p>
Read the book and laugh. It is not a real page turner, but
because it's short stories, one can read the book over some
time. It might just be to comic to read it all at once.</p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/0471418455/camillog-21"><img class="bot" src="../images/covers/datamanagement.jpg" alt="Data Management 3rd. Ed." /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=17">Data Management 3rd. Ed.</a></h4>
<p>by <i>Richard T. Watson</i></p>
<p class="ten">Genre: Non-fiction<br />
Reviewed: June 8, 2003</p>
<p>I don't normally review non-fiction, but since this book got stolen from me today I will remember it by this obituary. It's not like I loved
the book, it's about databases, probably one of the most unsexy areas in computing. If I ever end up in a job where I work with databases
would someone please shoot me? What I managed to read in it was not much. Today is 5 days from the database-exam so I should probably have
read more of it, but now it's gone. I hope the person who stole my bag gets bored to death from it.</p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/156280233X/camillog-21"><img class="bot" src="../images/covers/chimneyRock.jpeg" alt="Chimney Rock Blues" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=16">Chimney Rock Blues</a></h4>
<p>by <i>Janet McClellan</i></p>
<p class="ten">Genre: Lesbian fiction<br />
Reviewed: June 4, 2003</p>
<p>Man is this a bad book! Lesbian fiction is sometimes even worse than the short stories one finds in family
magazines. The detective Tru North(!) is the troubled woman working on a murder case while trying to figure out
which woman she loves the most. End of story. </p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/055299796X/camillog-21"><img class="bot" src="../images/covers/widow_for_year.jpeg" alt="A Widow for One Year" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=15">A Widow for One Year</a></h4>
<p>by <i>John Irving</i></p>
<p class="ten">Genre: Fiction<br />
Reviewed: March 18, 2003</p>
<p>  I have a weak spot for the novels by John Irving. This one is
so funny and at the same time very serious. A 16 year old boy is
seduced by a woman 20 years older than him and he keeps wanting
older women the rest of his life because he can never forget that
summer of lovemaking 60 times. The daughter (Ruth) of the older
woman grows up without her mother, but with her notorious
womanizer of a father. Ruth becomes a writer and accidentally
witnesses a murder that will mean a lot for the rest of her life.
</p>
<p>
  The book is a lot about sex, but also about death, mourning,
and letting life go on. A wise book that does not take
itself too seriously.</p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/0743231538/camillog-21"><img class="bot" src="../images/covers/shampoo_planet.jpeg" alt="Shampoo Planet&reg;" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=14">Shampoo Planet&reg;</a></h4>
<p>by <i>Douglas Coupland</i></p>
<p class="ten">Genre: Fiction<br />
Reviewed: February 3, 2003</p>
<p>It's about a guy called Tyler who lives with his hippie-mom
and his brother and sister in an uninteresting nuclear plant
town called Lancaster. He has been to Europe and in Paris he
met a girl who suddenly comes to visit him in Lancaster. She
turns his world upside down a least for a while. Tyler is a
modern ambitious consumer who talks and thinks in a very
American way, at least too American for me to understand. I
had a feeling the entire time while reading the book, that I
was missing the point. Still, I liked the book. There are
two passages in particular I like; one where after beating
somebody to a pulp he says: "I was nice to him for so long
and it never paid of". In the other passage Tyler starts
writing bon mots like "you are never far from the sound of
an engine" or "imagine yourself befriending a monster" on
one dollar bills. That is brilliant, what a medium!
Everybody uses money, an even better medium than billboards.</p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/0140074309/camillog-21"><img class="bot" src="../images/covers/other_women.jpeg" alt="Other Women" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=13">Other Women</a></h4>
<p>by <i>Lisa Alther</i></p>
<p class="ten">Genre: Fiction<br />
Reviewed: January 27, 2003</p>
<p>More than one of my friends would mention this book as the best lesbian book they have ever read. It is surely a very lesbian
book but I like almost all the other books written by Lisa Alther better. When I say very lesbian I mean: two women, a house
by a lake with three children, a cat, both sleep around, both are nurses, one goes into therapy, both always wear Levi's, one
weaves, the other knits and so on. </p>
<p> Don't get me wrong. I like books that have this air of lesbianism, but the whole therapy part just went right over my
head. I must be either a strong candidate for therapy or very much at ease with how I feel, because I kept thinking that the
therapy sessions where boring.</p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/014005894X/camillog-21"><img class="bot" src="../images/covers/original_sins.jpeg" alt="Original Sins" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=12">Original Sins</a></h4>
<p>by <i>Lisa Alther</i></p>
<p class="ten">Genre: Fiction<br />
Reviewed: January 27, 2003</p>
<p>The book follows five people from their childhood in Tennessee together into adulthood. The two girls, Sally and Emily, are
very different although they are sisters. Sally is the nice girl that only aims for a good husband, a neat home and being
popular. Emily on the other hand, likes sports, is intelligent and does well in school. She goes to New York to study and
there she has a bad marriage and some girlfriends. Raymond and Jed are brothers and as little a like as Sally and Emily. Jed
is the big sports jock that goes out with the nice girl (Sally) while Raymond also goes to New York only to discover that the
Yankees are a bunch of capitalist arseholes. He eventually moves back to the south where he prepares for the imminent
breakdown of the Yankee system by learning how to survive in a farm in the middle of nowhere. Last, but not least, Donny is
the grandson of the black maid working for Sally and Emily's parents. He is thought to "be a good nigger" by his grandmother,
because his mother had to leave town when he was young. She hit a white man in the head with a brick because he tried to rape
her. Fair enough I think, but apparently not in '59. Being a good nigger means that even though your whole family is
starving, the answer to "how are you?" is "I'm fine thank you". Luckily he slowly realizes that being a good nigger will not
really get him anywhere.</p>
<p>
Lisa Alther tells the story of these people in a way that makes them come very much alive. I simply ate this book, I could
not let it go because the story is so sweet, bitter, political and yet moderate, and funny. By far my favourite book written
by her.</p>

<a href="http://www.amazon.co.uk/exec/obidos/ASIN/0552771090/camillog-21"><img class="bot" src="../images/covers/4hand.jpeg" alt="The Fourth Hand" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=11">The Fourth Hand</a></h4>
<p>by <i>John Irving</i></p>
<p class="ten">Genre: Just fiction<br />
Reviewed: January 6, 2003</p>
<p>John Irving just has a way of getting away with really
absurd stuff casually. And I mean that in the good way! <br />

This book is about a journalist called Wallingford that gets
his hand eaten because he accidentally held the microphone
too close to a cage full of hungry lions. He has a problem
with women, not that he has trouble meeting them -they
virtually throw themselves at him, but he never seems to be
able to hold on to those that he likes. There is this one
woman who, when she sees the hand being eaten on TV (there
was a cameraman right next to Wallingford when the lions ate
his hand), decides that when her husband dies, Wallingford
can have his hand. At that time the husband is not dead,
actually he is a live and well, but after an ahem..
accident, he does die and the hand is transplanted to
Wallingford. <br />

Now the fun starts: The widow has some rather strange ideas
about her rights to that hand and the doctor that performs
the transplantation is a very funny person who plays
lacrosse with dog shits when he goes running along the
river. Wallingford gets more and more confused -poor guy, he
is really a decent guy, just not always very lucky.<br />
The Fourth Hand is not very long compared to The Cider House
Rules or other Irving books, and I must admit that I like
the long ones better. It's a good book but I found myself
missing the weird little anecdotes about everything. There
is just not room for a story with Irvinian commenting in 380
pages. </p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/1857236025/camillog-21"><img class="bot" src="../images/covers/paks2.jpg" alt="Divided Allegiance" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=10">Divided Allegiance</a></h4>
<p>by <i>Elizabeth Moon</i></p>
<p class="ten">Genre: Fantasy<br />
Reviewed: November 22, 2002</p>
<p>This is the second book in "The Deed of Paksenarrion"
series. As Paks is getting older and wiser she decides to
leave the mercenary army she was trained in to try her luck
on her own. She hooks up with an elf and they go (almost)
dungeon crawling together and Paks gets to use all her
swords skills. She is accepted as a paladin candidate with
the fellowship of Gird where she learns a lot of new things.
<br />
I like this book even better than book one and I can't wait
to read number three. The story is exciting and still told
in a very calm and pleasant way. Although it is a fantasy
world, nothing too amazing happens and that keeps the
credibility of the story high.</p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/1857236408/camillog-21"><img class="bot" src="../images/covers/paks1.jpeg" alt="Sheepfarmer's Daughter - The Deed of Paksenarrion" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=8">Sheepfarmer's Daughter - The Deed of Paksenarrion</a></h4>
<p>by <i>Elizabeth Moon</i></p>
<p class="ten">Genre: Fantasy<br />
Reviewed: November 5, 2002</p>
<p>It took me 8 years to read Tolkien's "The Hobbit". No
really, I thought it was annoying and they kept singing
songs in olden-hobbitian or something. I did have some idea
that I might like the three books in the Lord of The Rings
series, so I struggled hard to eat my way through The
Hobbit. And I did like the Lord of The Rings although they
kept singing too, I think only operas and musicals should
have this option, not books!. <br />
Anyway, The Sheepfarmer's Daughter is the first in the "The
Deed of Paksenarrion" series. It's set in a very
Tolkien-like world but there is no singing. Elizabeth Moon
is very good at building the characters in her book and as
Paksenarrion, a young woman that leaves home to become a
soldier in a mercenary army, learns more about the world so
does the reader. It's nice not to have every detail about
this fantasy world explained, it's simply viewed by the eyes
of Paksenarrion. <br />
This book is a lot better than the usual Dungeons &amp;
Dragons books, they tend to be violently dull and
unimaginative. If you were fascinated by the story in
Tolkien's books, but found yourself annoyed by the singing
and long descriptions of everything, you will like this
book. <br />
Thank you Tommy for recommending this book!</p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/014027877X/camillog-21"><img class="bot" src="../images/covers/1984.jpeg" alt="1984" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=7">1984</a></h4>
<p>by <i>George Orwell</i></p>
<p class="ten">Genre: Sci-fi, Classic<br />
Reviewed: October 19 2002</p>
<p>This book fathered the expression "Big Brother" (the one who is watching you). It's about a society where 
the need for controlling the citizens has gone mad. Everywhere and always people are being watched to register 
any political enemies. Written in 1949 he certainly foresaw the society we live in today. I'm thinking of Echelon, 
satellite monitering, and so on. I very much recommend this book.</p>


<a href="http://www.amazon.co.uk/exec/obidos/ASIN/0192801724/camillog-21"><img class="bot" src="../images/covers/dark_heart.jpeg" alt="Heart of Darkness and other Tales" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=6">Heart of Darkness and other Tales</a></h4>
<p>by <i>Joseph Conrad</i></p>
<p class="ten">Genre: Classic<br />
Reviewed: October 18 2002</p>
<p>Being a student of American Indian culture I am concerned with European and cultural imperialism. Joseph Conrad has got the cultural problems in colonies very right. The heart of Darkness is about a journey up the Congo River, but it could just as easily have been about a journey up the Amazonas River. Conrad&apos;s view of human nature is amusing as well as sad. </p>

<a href="http://www.amazon.co.uk/exec/obidos/ASIN/0752853694/camillog-21"><img class="bot" src="../images/covers/cat.jpeg" alt="The Unadulterated Cat" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=5">The Unadulterated Cat</a></h4>
<p>by <i>Terry Pratchett and Gary Jolliffe</i></p>
<p class="ten">Genre: Humour<br />
Reviewed: October 18 2002</p>
<p>To Terry Pratchett a real cat is a cat that doesn't eat from a bowl, but it can hear the fridge door being opened 200 meters away. It certainly never appears on Christmas and birthday cards and its name would typically be something like "Yaargeroffoutofityarbarstard".<br /> 
This book covers everything from how to get a real cat, how to understand your real cats needs like food, sex, and entertainment. Mr. Pratchett must be a real cat lover, because he certainly captured the essence of how cats behave. The writing style of this book is funny and not as annoying as the Discworld-series. Gary Jolliffe's cartoon drawings are also very funny.</p>

<a href="http://www.amazon.co.uk/exec/obidos/ASIN/1555834612/camillog-21"><img class="bot" src="../images/covers/femmes_guide.jpeg" alt="The Femme's Guide to the Universe" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=4">The Femme's Guide to the Universe</a></h4>
<p>by <i>Shar Rednour</i></p>
<p class="ten">Genre: Humour, Handbook<br />
Reviewed: October 18 2002</p>
<p>Shar is a real queen and she humorously guides her readers trough such useful subjects as how to make your girlfriend think you really did the ready mix pancakes from scratch, how to make your apartment look like a castle, and how to fuck in high heels. This book contains very important knowledge for all femmes -and the rest of us who aren't really in to the whole butch femme thing. Its good fun reading it.</p>

<a href="http://www.amazon.co.uk/exec/obidos/ASIN/1857231813/camillog-21"><img class="bot" src="../images/covers/someone_taller.jpeg" alt="Expecting Someone Taller" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=3">Expecting Someone Taller</a></h4>
<p>by <i>Tom Holt</i></p>
<p class="ten">Genre: Fantasy Crazy<br />
Reviewed: October 16 2002</p>
<p>I laughed for an entire day when I read this book. It's a lot like the books Terry Pratchet writes, only this is good. The book is a sort of sequel to the composer Wagner's work "Ring des Niebelungen" and then again it's almost too crazy. The main character Malcolm runs over a badger in his car. The battered badger turns out to be Ingolf, the last of the Giants and he talks. Just before the badger dies, he gives Malcolm the power to rule the entire world. He finds himself in a cast of Niebelunger characters giving exactly as much trouble as the ruler of the earth is expected to have.
Very British humour and a great laugh!</p>

<a href="http://www.amazon.co.uk/exec/obidos/ASIN/0099734419/camillog-21"><img class="bot" src="../images/covers/the_passion.jpeg" alt="The Passion" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=2">The Passion</a></h4>
<p>by <i>Jeanette Winterson</i></p>
<p class="ten">Genre: Lesbian novel<br />
Reviewed: October 16 2002</p>
<p>Jeanette Winterson has been one of my favourites since I saw "Oranges are not the only fruit" on TV when I was little. I've read a lot of her books but this one is the one I like best.
Set in Venice in the time of Napoleon the mysterious heroine who has webbed feet passionately falls in love with a rich Venetian woman. Henri, Napoleons personal cook marches to the Russian front and almost dies of cold and passion. 
This book is the incarnation of passion, the title says exactly what this book is. </p>

<a href="http://www.amazon.co.uk/exec/obidos/ASIN/1857231589/camillog-21"><img class="bot" src="../images/covers/rama_I.jpeg" alt="Rendezvous with Rama" /></a>
<h4><a href="http://xoc.dk/older/books/default.php?id=1">Rendezvous with Rama</a></h4>
<p>by <i>Arthur C. Clarke</i></p>
<p class="ten">Genre: Sci-fi<br />
Reviewed: October 15 2002</p>
<p>I always thought that Arthur C. Clarke was just the boring guy on the Discovery channel who talked about inexplicable things. I was very wrong. This book is so interesting and brilliant that I read it in one day. 
The year is 2130 and humans discover what they think is a giant meteor approaching earth. They name the meteor Rama, but the soon discover that it is too big and too regular in shape to be meteor. A spaceship lands on it to explore the alien space vessel. The cylinder is hollow and inside, this is the brilliant part, is a landscape with gravity because the vessel is spinning. There is a "Cylindrical Sea" and weird buildings resembling skyscrapers but no living things in sight except for some "biots" -robots maintaining the landscape. 
The concept of "a world inside out" is fascinating as is Clarke's down-to-earth (ha ha) way of writing. Although the man clearly is genius there is nothing too difficult to understand or technical ramblings in his book. 
Even if you don't like science fiction I would recommend this book anytime!
This book is the first in a series of four. It can be read without reading the rest, but chances are you will be craving for the next three.</p>

